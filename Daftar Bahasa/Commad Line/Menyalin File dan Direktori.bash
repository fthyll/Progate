1 Pindah ke direktori languages di dalam direktori awal 
~ $ cd languages

2 Pindah ke direktori html 
languages $ cd html

3 Salin dojo.txt dan buat project.txt
html $ cp dojo.txt project.txt

4 Tampilkan file di dalam direktori html 
html $ ls

